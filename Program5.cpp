#include<iostream>
using namespace std;
int main()
{
    // Increment and Decrement Operators
    int x = 4;
    cout<<x<<endl;
    x++; //Post Increment
    cout<<x<<endl; 
    //x = x + 1
    //x += 1
    //x++
    x--; //Post Decrement
    cout<<x<<endl;
    ++x; //Pre Increment
    cout<<x<<endl;
    --x; //Pre Decrement
    cout<<x<<endl;
    return 0;
}